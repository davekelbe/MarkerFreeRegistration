function [st,alpha,perc] = studentvalue(a,n)
%% This function retrieve the student
% distribution value for a given alpha and
% degree of freedom value.
%
% File created on Mar 5th 2008
%
% Last modified: Jan 29th 2009
%
% Author: Arnaud Laurent
%
%   Inputs: a = can be any alpha value
%           n = number of data points
%
%   Output: st = value of the student distribution
%           alpha = the alpha value used
%           perc = the corresponding percentage
%
%	Comment: a can be expressed in percent
%
df = n-2; % Degree of freedom

ttable = ini_ttable;

row = cls(ttable(:,1),df);
row=row(1);

if a>1
    col = cls(ttable(end,:),a);
else
    col = cls(ttable(1,:),a);
end
col=col(2);

st = ttable(row,col);
alpha = ttable(1,col);
perc = ttable(end,col);

%%%%%
function idx = cls(x,a)
%% This function retrieve the column and row
% of the closest value to a in x

[idx(1),idx(2)] = find(abs(x-a)==nanmin(nanmin(abs(x-a))));

